// Fungsi ini merupakan method dari sebuah class
// FNV-1 Hash (32-bit)
public static uint FNVHash (string key)
{
    uint state = 0x811C9DC5;                // 2166136261
    uint i = 0;

    for (i = 0; i < key.Length; i++)
    {
        state = state * 0x1000193;          // 16777619
        state = state ^ ((byte)key[(int)i]);
    }
    return state;
}

